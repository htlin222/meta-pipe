def generate_validation_report(project_root: Path, strict: bool = False) -> Dict:
    """Generate comprehensive NMA validation report."""

    # Extract expected treatments
    extraction_csv = project_root / "05_extraction" / "round-01" / "extraction.csv"

    if not extraction_csv.exists():
        return {"valid": False, "error": f"Extraction data not found: {extraction_csv}"}

    expected_treatments = extract_treatments_from_extraction(extraction_csv)

    if len(expected_treatments) < 3:
        return {
            "valid": False,
            "error": f"Only {len(expected_treatments)} treatments found - NMA requires ≥3",
        }

    # Run all checks
    checks = [
        ("Network graph", check_network_graph, [project_root, expected_treatments]),
        ("League table", check_league_table, [project_root, expected_treatments]),
        ("SUCRA rankings", check_sucra_rankings, [project_root, expected_treatments]),
        ("Inconsistency assessment", check_inconsistency_assessment, [project_root]),
        ("CINeMA GRADE", check_cinema_assessment, [project_root]),
        ("Bayesian convergence", check_bayesian_convergence, [project_root]),
        ("Sensitivity analysis", check_sensitivity_analysis, [project_root]),
        ("CNMA (component NMA)", check_cnma_outputs, [project_root]),
        ("NMA meta-regression", check_meta_regression_outputs, [project_root]),
        ("Transitivity tests", check_transitivity_tests, [project_root]),
    ]

    results = {
        "n_treatments": len(expected_treatments),
        "treatments": sorted(expected_treatments),
        "checks": {},
        "all_issues": [],
        "valid": True,
    }

    for name, check_func, args in checks:
        passed, issues = check_func(*args)
        results["checks"][name] = {"passed": passed, "issues": issues}
        results["all_issues"].extend(issues)

        if not passed and strict:
            results["valid"] = False

    return results


def format_validation_report(results: Dict) -> str:
    """Format validation results as markdown."""

    if "error" in results:
        return f"# NMA Validation Report\n\n❌ **Error**: {results['error']}\n"

    lines = [
        "# NMA Output Validation Report",
        "",
        f"**Treatments**: {results['n_treatments']} ({', '.join(results['treatments'])})",
        f"**Expected Comparisons**: {results['n_treatments'] * (results['n_treatments'] - 1) // 2}",
        "",
        "---",
        "",
        "## Validation Checks",
        "",
    ]

    for check_name, check_data in results["checks"].items():
        status = "✅" if check_data["passed"] else "❌"
        lines.append(f"### {status} {check_name}")

        if check_data["issues"]:
            lines.append("")
            for issue in check_data["issues"]:
                lines.append(f"- {issue}")
        else:
            lines.append("")
            lines.append("✅ All checks passed")

        lines.append("")

    # Summary
    lines.extend(["---", "", "## Summary", ""])

    passed_count = sum(1 for c in results["checks"].values() if c["passed"])
    total_count = len(results["checks"])

    lines.append(f"**Passed**: {passed_count}/{total_count} checks")
    lines.append("")

    if results["valid"]:
        lines.append("✅ **NMA outputs valid for publication**")
    else:
        lines.append("❌ **Critical issues found - address before submission**")

    lines.extend(
        [
            "",
            "---",
            "",
            "## Required NMA Outputs for Publication",
            "",
            "Per PRISMA-NMA checklist:",
            "",
            "- [ ] Network graph (geometry)",
            "- [ ] League table (all pairwise comparisons)",
            "- [ ] Treatment rankings (SUCRA/P-scores with uncertainty)",
            "- [ ] Inconsistency assessment (node-splitting + design-by-treatment)",
            "- [ ] CINeMA GRADE assessment (intransitivity + incoherence)",
            "- [ ] Bayesian convergence diagnostics (if gemtc used)",
            "- [ ] Sensitivity analysis (frequentist concordance)",
            "",
        ]
    )

    return "\n".join(lines)


def main():
    parser = argparse.ArgumentParser(
        description="Validate NMA outputs for publication readiness",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  uv run validate_nma_outputs.py --root projects/ici-nsclc
  uv run validate_nma_outputs.py --root projects/ici-nsclc --out 09_qa/nma_validation.md
  uv run validate_nma_outputs.py --root projects/ici-nsclc --strict
        """,
    )

    parser.add_argument("--root", required=True, help="Project root directory")
    parser.add_argument("--out", help="Output markdown file (default: stdout)")
    parser.add_argument(
        "--strict",
        action="store_true",
        help="Fail validation if any check fails (exit code 1)",
    )

    args = parser.parse_args()

    project_root = Path(args.root)
    if not project_root.exists():
        print(f"Error: Project root not found: {project_root}", file=sys.stderr)
        sys.exit(1)

    # Generate validation report
    results = generate_validation_report(project_root, args.strict)

    # Format report
    report = format_validation_report(results)

    # Output
    if args.out:
        out_path = Path(args.out)
        out_path.parent.mkdir(parents=True, exist_ok=True)
        out_path.write_text(report)
        print(f"✅ NMA validation report written to: {out_path}")
    else:
        print(report)

    # Exit code
    if args.strict and not results.get("valid", False):
        sys.exit(1)


if __name__ == "__main__":
    main()
